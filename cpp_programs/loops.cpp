#include<iostream>
using namespace std;
/**********************************************************
*  Loops: for, while, do while.                           *
**********************************************************/
int main(){
  int i=0;
  for(int i=0;i<10;i++){
	  cout<<i<<endl;
  }
  cout<< "\n\nWhile loop:"<<endl;
  while(i<5){
	  cout<<i<<endl;
	  i++;
  }
  return 0;
  }